def _store_thread(app):
    store = app.state.conversation_store
    return asyncio.run(store.get_thread(SESSION_ID, client_code=CLIENT_CODE))


def test_ticket_route_requires_credentials(app):
    with TestClient(app) as client:
        resp = client.post("/api/ticket", json={"reason": "General Query"})
    assert resp.status_code == 400
    assert resp.json() == {"error": "MISSING_CREDENTIALS"}


@respx.mock
def test_ticket_route_success_appends_memo(app, monkeypatch):
    _fd_env(monkeypatch)
    route = respx.post(f"{FD_ROOT}/tickets").mock(
        return_value=httpx.Response(201, json={"id": 7551234})
    )
    with TestClient(app) as client:
        thread = _store_thread(app)
        app.state.conversation_store.append_turn(
            thread, role="user", kind="user_text",
            content=_text("the report is wrong"),
        )
        resp = client.post(
            "/api/ticket", headers=HEADERS, json={"reason": "General Query"}
        )
        assert resp.status_code == 200
        assert resp.json() == {"ticketId": 7551234, "status": "Open"}

        # the conversation travelled in the description
        sent = json.loads(route.calls.last.request.content)
        assert "the report is wrong" in sent["description"]
        # the flow-event memo landed on the thread
        memo = thread.turns[-1]
        assert memo.kind == "flow_event"
        assert memo.content[0]["text"] == ticket_memo(7551234, "General Query")
        assert memo.meta == {
            "flow": "ticket", "reason": "General Query", "ticketId": 7551234,
        }


