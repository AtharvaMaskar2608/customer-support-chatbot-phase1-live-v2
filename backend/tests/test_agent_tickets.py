# --- ticket core: payload contract (design D1) --------------------------------


def _ctx(http_client, thread=None):
    return ToolCtx(
        session_id=SESSION_ID,
        sso_jwt=SSO_JWT,
        client_code=CLIENT_CODE,
        http_client=http_client,
        thread=thread,
    )


async def _run(params, thread=None):
    async with httpx.AsyncClient() as http:
        return await run_raise_ticket(params, _ctx(http, thread=thread))


@respx.mock
def test_payload_matches_the_cloned_contract(monkeypatch):
    _fd_env(monkeypatch)
    route = respx.post(f"{FD_ROOT}/tickets").mock(
        return_value=httpx.Response(201, json={"id": 7551234, "status": 2})
    )
    result = asyncio.run(
        _run({"reason": "General Query"}, thread=_conversation_thread())
    )
    assert result == {"kind": "ticket", "ticketId": 7551234, "status": "Open"}

    request = route.calls.last.request
    sent = json.loads(request.content)
    description = sent.pop("description")
    # Byte-for-byte the D1 parameter set — requester is the client code only,
    # no email/phone fields anywhere.
    assert sent == {
        "subject": f"[Choice Jini] General Query — Client {CLIENT_CODE}",
        "unique_external_id": CLIENT_CODE,
        "name": CLIENT_CODE,
        "status": 2,
        "priority": 2,
        "source": 7,
        "type": "GENERAL QUERY",
        "group_id": 22000168676,
        "tags": ["choice-jini", "chatbot-testing", "lang:en"],
        "custom_fields": {
            "cf_client_id": CLIENT_CODE,
            "cf_source": "chat box",
            "cf_product": "finx",
            "cf_query_type149508": "finx-bot",
            "cf_query_sub_type": "finx-bot-test",
        },
    }
    assert "email" not in sent and "phone" not in sent
    # description = the transcript, credential-clean
    assert "<b>Client:</b>" in description
    assert SESSION_ID not in description and SSO_JWT not in description
    # basic auth: API key + "X", never logged, never in the body
    expected = base64.b64encode(f"{FD_KEY}:X".encode()).decode()
    assert request.headers["authorization"] == f"Basic {expected}"
    assert FD_KEY not in request.content.decode()


